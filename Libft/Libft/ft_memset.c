#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	int	i;

	i = 0;
	while (len > 0)
	{
		*(unsigned char *)b = c;
		i++;
		b++;
		len--;
	}
	return (b - i);
}
